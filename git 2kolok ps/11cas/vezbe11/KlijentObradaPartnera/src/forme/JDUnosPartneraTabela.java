    private void srediFormu() {
        try {
            PartneriTableModel ptm = new PartneriTableModel(new ArrayList());
            tblPartneri.setModel(ptm);  
            
            ZahtevKlienta zahtev = new ZahtevKlienta();
            zahtev.setOperacija(ZahtevKlienta.VRATI_SVA_MESTA);
            Komunikacija.vratiInstancu().posaljiZahtev(zahtev);
            OdgovorServera odgovor = Komunikacija.vratiInstancu().procitajOdgovor();
            if(odgovor.getSignal()==-1){
                throw odgovor.getIzuzetak();
            }
            List<Mesto> mesta = (List<Mesto>) odgovor.getObjekatIzvrsenjaOperacije();
            
            JComboBox cbMesto = new JComboBox(mesta.toArray());
            
            TableColumn tcMesto = tblPartneri.getColumnModel().getColumn(5);
            tcMesto.setCellEditor(new DefaultCellEditor(cbMesto));
            
            tcMesto.setPreferredWidth(200);
            
            
            
        } catch (Exception ex) {
            Logger.getLogger(JDUnosPartneraTabela.class.getName()).log(Level.SEVERE, null, ex);
        }
        
        
    }
