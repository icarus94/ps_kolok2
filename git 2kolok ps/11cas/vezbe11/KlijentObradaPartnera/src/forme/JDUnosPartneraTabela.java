    private void btnSacuvajPartnereActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSacuvajPartnereActionPerformed
        try {
            // TODO add your handling code here:
            PartneriTableModel ptm = (PartneriTableModel) tblPartneri.getModel();
            List<PoslovniPartner> partneri = ptm.vratiListuPartnera();
            
            //KontrolerPL.vratiInstancu().sacuvajListuPartnera(partneri);
            
            ZahtevKlienta zahtev = new ZahtevKlienta();
            zahtev.setOperacija(ZahtevKlienta.SACUVAJ_LISTU_POSLOVNIH_PARTNERA);
            zahtev.setObjekatOperacije(partneri);
            Komunikacija.vratiInstancu().posaljiZahtev(zahtev);
            OdgovorServera odgovor = Komunikacija.vratiInstancu().procitajOdgovor();
            if(odgovor.getSignal()==-1){
                throw odgovor.getIzuzetak();
            }
            
            
            JOptionPane.showMessageDialog(rootPane, "USPESNO CUVANJE LISTE PARTNERA", "CUVANJE PARTNERA", JOptionPane.INFORMATION_MESSAGE);
            
        } catch (Exception ex) {
            Logger.getLogger(JDUnosPartneraTabela.class.getName()).log(Level.SEVERE, null, ex);
            JOptionPane.showMessageDialog(rootPane, "GRESKA:"+ex.getMessage(), "CUVANJE PARTNERA", JOptionPane.INFORMATION_MESSAGE);
        }
        
         
        
    }//GEN-LAST:event_btnSacuvajPartnereActionPerformed

